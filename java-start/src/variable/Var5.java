package variable;

public class Var5 {
    public static void main(String[] args) {
        //변수 선언, 초기화 각각 다로
        int a;
        a = 1;
        System.out.println(a);

        int b = 2;// 선언, 초기화 동시
        System.out.println(b);

        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
